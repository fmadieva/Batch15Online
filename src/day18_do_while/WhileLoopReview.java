package day18_do_while;

public class WhileLoopReview {

    public static void main(String[] args){

        int x = 10;

        while (x > 5) {
            System.out.println(x + " Its more than 5");
            --x;
        }

        System.out.println("THE END");

        // 10 Its more than 5
        //9 Its more than 5
        //8 Its more than 5
        //7 Its more than 5
        //6 Its more than 5
        //THE END







    }
}
