package day07_if_else_statement;

public class RelationalOperator {

    public static void main(String[] args){

        // > >= < <= == != Relational Operator

        //Relational Operator is used to compare 2 opr
        //and return true or false
        // anything action or expression
        // //that return true or false result
        // can be called boolean expression

        System.out.println(7 > 5);
        System.out.println(7 < -19);
        System.out.println(5 >= 5);
        System.out.println(7 >= 8);
        System.out.println(4 <= 5);
        System.out.println(18 >= 18);

        // == is used to check for equality
        // and very different from single =

        //difference between == and = same as difference between equal and equality in daily English
        System.out.println(5 == 5);

        // != is used to check inequality
        // in programming translate to Not
        // (is six not equal to 10) true
        System.out.println(6 != 10);

        // == and != are questions, checking for equality or not equality





    }
}
