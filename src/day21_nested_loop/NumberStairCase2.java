package day21_nested_loop;

public class NumberStairCase2 {

    public static void main(String[] args){

        for (int x = 1; x <= 30; x++) {
            //System.out.println("x  :" + x );


            for (int y = 1; y <= x; y++) {
                System.out.print(y + " ");
            }
            System.out.println();


        }
// run it. very cool!



     






    }
}
